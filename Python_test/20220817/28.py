# 관계 연산자(비교 연산자)

# 크다(>)
print(2>1)
print(1>2)
print()

# 작다(<)
print(2<1)
print(1<2)
print()

# 같다(==)
print(1==1)
print(1==2)
print(1=='1')
print()

# 다르다, 같지 않다(!+)
print(1!=1)
print(1!=2)
print(1!='1')
print()

# 크거나 같다(>=)
print(1>=1)
print(1>=0)
print()

# 작거나 같다(<=)
print(1<=2)
print(1<=1)
print()

# 관계 연산자는 Bool 값을 반환
