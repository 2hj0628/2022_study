# 복합 대입 연산자

# 덧셈
num1=5
num1+=2     #num1=num1+2
print(num1)

# 뺄셈
num2=5
num2-=2     #num2=num2-2
print(num2)

# 곱셈
num3=5
num3*=3     #num3=num3*3
print(num3)

# 나눗셈
num4=5
num4/=3     #num4=num4/3
print(round(num4,3))
print(num4)

# 몫
num5=5
num5//=3        #num5=num5//3
print(num5)

# 나머지
num6=5
num6%=3        #num6=num6%3
print(num6)

# 제곱
num7=5
num7**=3        #num7=num7**3
print(num7)