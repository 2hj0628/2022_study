# 접두어를 활용해 2,8,16진수 등을 표현 가능

num1=12345      #10진수
print(type(num1))
print(num1)
print()

num2=0b11       #2진수 : 0,1로 표현
print(type(num2))
print(num2)
print()

num3=0o12       #8진수 : 0~7까지 표현
print(type(num3))
print(num3)
print()

num4=0x23       #16진수 : 0~9, A~F로 표현
print(type(num4))
print(num4)
print()